package com.first;

public class swap
{
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("a="+a);
        System.out.println("b="+b);
        int c;
        c=a;
        a=b;
        b=c;
        System.out.println("a="+a);
        System.out.println("b="+b);

    }
}
