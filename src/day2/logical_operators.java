package day2;

class logicalOperators {
    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        System.out.println(a == b);// equal
        System.out.println(a < b);//lesser
        System.out.println(a > b);//greater
        System.out.println(a <= b);//lesser than equal
        System.out.println(a >= b);//greater than equal
        System.out.println(a != b);//not equals

    }
}

