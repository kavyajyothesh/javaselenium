package day2;

public class String_8 {
    public static void main(String[] args) {
        String name = "Bangalore";
        System.out.println(name.isEmpty());
        System.out.println(name.startsWith("Bang"));
        System.out.println(name.endsWith("ore"));
        System.out.println(name.equals("Bangalore"));
        System.out.println(name.equalsIgnoreCase("bangalore"));
        System.out.println(name.contains("lore"));
        System.out.println(name.contains("clore"));














    }}
